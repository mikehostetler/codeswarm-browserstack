module.exports = serialize(browsers);

function serialize(browsers) {
  var ret = [];
  Object.keys(browsers).forEach(function(os) {
    Object.keys(browsers[os]).forEach(function(os_version) {
      Object.keys(browsers[os][os_version]).forEach(function(browser) {
        browsers[os][os_version][browser].forEach(function(browser_version) {
          ret.push([os, os_version, browser, browser_version].join('-'));
        });
      });
    });
  });
  return ret;
}